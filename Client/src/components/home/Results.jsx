import React, { useEffect, useMemo, useRef, useState } from "react";
import { Award, ChevronDown, ChevronUp, CheckCircle2, ImageIcon, FileText, BarChart2 } from "lucide-react";

function Results({ student_name, roll_number, comparisons }) {
  const [isSaved, setIsSaved] = useState(false);
  const [saving, setSaving] = useState(false);
  const [showDetails, setShowDetails] = useState(false);

  // ✅ Prevent Auto-save running twice in React StrictMode
  const hasSavedOnce = useRef(false);

  // ✅ Calculations
  const { cumulativeMarks, maxTotalMarks, percentage } = useMemo(() => {
    const cumulative = Object.values(comparisons).reduce(
      (total, result) => total + (result.total_score || 0),
      0
    );

    const maxTotal = Object.keys(comparisons).length * 10;
    const percent =
      maxTotal > 0 ? ((cumulative / maxTotal) * 100).toFixed(2) : "0.00";

    return {
      cumulativeMarks: cumulative,
      maxTotalMarks: maxTotal,
      percentage: percent,
    };
  }, [comparisons]);

  // ✅ Payload
  const reportData = useMemo(() => {
    const pageMarks = Object.entries(comparisons).map(([page, result]) => ({
      page,
      marks: result.total_score,
    }));

    return {
      student_name,
      roll_number,
      total_marks: cumulativeMarks,
      max_marks: maxTotalMarks,
      percentage,
      page_marks: pageMarks,
      details: comparisons,
    };
  }, [student_name, roll_number, cumulativeMarks, maxTotalMarks, percentage, comparisons]);

  // ✅ Auto Save only ONCE
  useEffect(() => {
    const autoSave = async () => {
      if (!student_name || !roll_number) return;
      if (!comparisons || Object.keys(comparisons).length === 0) return;

      if (hasSavedOnce.current) return;
      hasSavedOnce.current = true;

      setSaving(true);
      try {
        const response = await fetch("http://127.0.0.1:5000/save-report", {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify(reportData),
        });

        if (response.ok) {
          setIsSaved(true);
        } else {
          console.log("Failed to auto-save report");
        }
      } catch (error) {
        console.error("Auto-save error:", error);
      } finally {
        setSaving(false);
      }
    };

    autoSave();
  }, [student_name, roll_number, comparisons, reportData]);

  // ✅ Badge showing evaluation type per page
  const EvalTypeBadge = ({ type }) => {
    if (type === "diagram") {
      return (
        <span className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full text-xs font-semibold bg-purple-100 text-purple-700 border border-purple-200">
          <ImageIcon className="w-3 h-3" /> Diagram
        </span>
      );
    }
    if (type === "image") {
      return (
        <span className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full text-xs font-semibold bg-blue-100 text-blue-700 border border-blue-200">
          <ImageIcon className="w-3 h-3" /> Image
        </span>
      );
    }
    if (type === "text") {
      return (
        <span className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full text-xs font-semibold bg-green-100 text-green-700 border border-green-200">
          <FileText className="w-3 h-3" /> Text Only
        </span>
      );
    }
    if (type === "none") {
      return (
        <span className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full text-xs font-semibold bg-gray-100 text-gray-500 border border-gray-200">
          <FileText className="w-3 h-3" /> Not Detected
        </span>
      );
    }
    // fallback
    return (
      <span className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full text-xs font-semibold bg-gray-100 text-gray-500 border border-gray-200">
        <FileText className="w-3 h-3" /> Text Only
      </span>
    );
  };

  // ✅ Reusable progress bar
  const ScoreBar = ({ value, max = 100, color = "bg-indigo-600" }) => {
    const pct = max > 0 ? Math.min((value / max) * 100, 100) : 0;
    return (
      <div className="flex flex-col items-center gap-1">
        <div className="h-2 w-28 bg-gray-200 rounded-full overflow-hidden">
          <div className={`h-full ${color}`} style={{ width: `${pct}%` }} />
        </div>
        <span className="text-sm font-semibold text-gray-700">
          {Number(value).toFixed(2)}{max === 100 ? "%" : ""}
        </span>
      </div>
    );
  };

  return (
    <div className="space-y-8 text-gray-900">

      {/* ── SUMMARY CARD ── */}
      <div className="bg-white/70 backdrop-blur-xl border border-white/30 rounded-3xl shadow-lg p-8">
        <div className="flex items-start justify-between flex-wrap gap-6">

          <div>
            <h2 className="text-2xl sm:text-3xl font-extrabold flex items-center gap-2">
              <Award className="w-7 h-7 text-indigo-600" />
              Student Report
            </h2>
            <p className="text-gray-600 mt-2">AI evaluation generated by GradeX.</p>

            <div className="mt-4">
              {saving && (
                <p className="text-sm text-gray-500">Saving report to database...</p>
              )}
              {isSaved && (
                <p className="text-sm font-semibold text-green-600 flex items-center gap-2">
                  <CheckCircle2 className="w-4 h-4" />
                  Report saved successfully ✅
                </p>
              )}
            </div>

            <div className="mt-5 space-y-2 text-sm sm:text-base">
              <p><span className="font-semibold text-gray-700">Student Name:</span> {student_name}</p>
              <p><span className="font-semibold text-gray-700">Roll Number:</span> {roll_number}</p>
              <p>
                <span className="font-semibold text-gray-700">Total Marks:</span>{" "}
                <span className="font-extrabold text-indigo-600">{cumulativeMarks} / {maxTotalMarks}</span>
              </p>
            </div>
          </div>

          <div className="min-w-[260px] w-full sm:w-auto">
            <p className="text-sm font-semibold text-gray-700 mb-2 text-center">Overall Performance</p>
            <div className="bg-gray-100 rounded-2xl p-6 text-center border border-gray-200">
              <div className="text-4xl font-extrabold bg-gradient-to-r from-indigo-600 to-purple-600 bg-clip-text text-transparent">
                {percentage}%
              </div>
              <div className="mt-4">
                <div className="h-3 w-full bg-gray-200 rounded-full overflow-hidden">
                  <div
                    className="h-full bg-gradient-to-r from-indigo-600 to-purple-600"
                    style={{ width: `${percentage}%` }}
                  />
                </div>
              </div>
              <p className="text-xs text-gray-500 mt-3">Performance score based on AI comparison</p>
            </div>
          </div>

        </div>
      </div>

      {/* ── TOGGLE BUTTON ── */}
      <div className="flex justify-center">
        <button
          type="button"
          onClick={() => setShowDetails((prev) => !prev)}
          className="px-6 py-3 rounded-xl bg-white text-gray-800 font-semibold shadow-sm border border-gray-200 hover:bg-gray-50 transition flex items-center justify-center gap-2"
        >
          {showDetails ? (
            <><ChevronUp className="w-5 h-5" /> Hide Detailed Analysis</>
          ) : (
            <><ChevronDown className="w-5 h-5" /> View Detailed Analysis</>
          )}
        </button>
      </div>

      {/* ── DETAILED ANALYSIS ── */}
      {showDetails && (
        <div className="space-y-8">
          {Object.entries(comparisons).map(([page, result]) => {
            const isDiagram = result.evaluation_type === "diagram";
            const isImage   = result.evaluation_type === "image";
            const isText    = result.evaluation_type === "text" || result.evaluation_type === "none";
            const hasVisual = isDiagram || isImage;
            // Mixed = both text and visual were evaluated
            const hasText   = hasVisual
              ? (result.text_marks > 0 || result.student_text)
              : true;

            return (
              <div
                key={page}
                className="bg-white/70 backdrop-blur-xl border border-white/30 rounded-3xl shadow-md p-8"
              >
                {/* Page header + badge */}
                <div className="flex items-center justify-between flex-wrap gap-3 mb-6">
                  <h3 className="text-xl sm:text-2xl font-extrabold">
                    Question {page} — Detailed Analysis
                  </h3>
                  <EvalTypeBadge type={result.evaluation_type} />
                </div>

                {/* ── TEXT EVALUATION — only shown when question has text ── */}
                {(!hasVisual || hasText) && (
                  <>
                    <p className="text-xs font-bold uppercase tracking-widest text-gray-400 mb-2 flex items-center gap-1">
                      <FileText className="w-3.5 h-3.5" /> Text Evaluation
                    </p>
                    <div className="overflow-x-auto rounded-2xl border border-gray-100 bg-white mb-6">
                      <table className="min-w-full table-auto border-collapse">
                        <thead>
                          <tr className="bg-gradient-to-r from-indigo-600 to-purple-600 text-white">
                            <th className="py-3 px-5 text-center text-sm font-bold uppercase">Student Answer</th>
                            <th className="py-3 px-5 text-center text-sm font-bold uppercase">Reference Answer</th>
                            <th className="py-3 px-5 text-center text-sm font-bold uppercase">Similarity</th>
                            <th className="py-3 px-5 text-center text-sm font-bold uppercase">Context</th>
                            <th className="py-3 px-5 text-center text-sm font-bold uppercase">Text Marks</th>
                          </tr>
                        </thead>
                        <tbody>
                          <tr className="border-t hover:bg-indigo-50/40 transition">
                            <td className="py-4 px-5 text-sm text-gray-700 max-w-xs">
                              {result.student_text || "N/A"}
                            </td>
                            <td className="py-4 px-5 text-sm text-gray-700 max-w-xs">
                              {result.teacher_text || "N/A"}
                            </td>
                            <td className="py-4 px-5 text-center">
                              <ScoreBar value={result.similarity_score ?? 0} color="bg-indigo-600" />
                            </td>
                            <td className="py-4 px-5 text-center">
                              <ScoreBar value={result.contextual_score ?? 0} color="bg-purple-600" />
                            </td>
                            <td className="py-4 px-5 text-center">
                              <span className="px-3 py-1 rounded-lg bg-indigo-50 text-indigo-700 font-extrabold">
                                {result.text_marks ?? "N/A"} / 10
                              </span>
                            </td>
                          </tr>
                        </tbody>
                      </table>
                    </div>
                  </>
                )}

                {/* ── DIAGRAM / IMAGE EVALUATION (only when visual exists) ── */}
                {hasVisual && (
                  <>
                    <p className="text-xs font-bold uppercase tracking-widest text-gray-400 mb-2 flex items-center gap-1">
                      <BarChart2 className="w-3.5 h-3.5" />
                      {isDiagram
                        ? "Diagram Evaluation (CLIP + DINO + ORB + OCR Labels)"
                        : "Image Evaluation (SSIM)"}
                    </p>

                    <div className="overflow-x-auto rounded-2xl border border-gray-100 bg-white mb-6">
                      <table className="min-w-full table-auto border-collapse">
                        <thead>
                          <tr className={`text-white ${isDiagram
                            ? "bg-gradient-to-r from-purple-600 to-pink-500"
                            : "bg-gradient-to-r from-blue-500 to-cyan-500"}`}
                          >
                            <th className="py-3 px-5 text-center text-sm font-bold uppercase">
                              {isDiagram ? "Diagram Similarity" : "Image Similarity"}
                            </th>
                            <th className="py-3 px-5 text-center text-sm font-bold uppercase">
                              {isDiagram ? "Diagram Marks" : "Image Marks"}
                            </th>
                          </tr>
                        </thead>
                        <tbody>
                          <tr className="border-t hover:bg-purple-50/40 transition">
                            <td className="py-4 px-5 text-center">
                              <ScoreBar
                                value={(result.image_similarity ?? 0) * 100}
                                color={isDiagram ? "bg-purple-600" : "bg-blue-500"}
                              />
                            </td>
                            <td className="py-4 px-5 text-center">
                              <span className={`px-3 py-1 rounded-lg font-extrabold ${
                                isDiagram
                                  ? "bg-purple-50 text-purple-700"
                                  : "bg-blue-50 text-blue-700"
                              }`}>
                                {result.image_marks ?? 0} / 10
                              </span>
                            </td>
                          </tr>
                        </tbody>
                      </table>
                    </div>
                  </>
                )}

                {/* ── FINAL SCORE BAR ── */}
                <div className="flex items-center justify-between bg-gradient-to-r from-indigo-50 to-purple-50 border border-indigo-100 rounded-2xl px-6 py-4">
                  <p className="text-sm font-semibold text-gray-600">
                    Final Score
                    <span className="ml-2 text-xs text-gray-400 font-normal">
                      {isText && "(Text 100%)"}
                      {hasVisual && !hasText && `(${isDiagram ? "Diagram" : "Image"} 100%)`}
                      {hasVisual && hasText && `(Text 70% + ${isDiagram ? "Diagram" : "Image"} 30%)`}
                    </span>
                  </p>
                  <span className="text-2xl font-extrabold bg-gradient-to-r from-indigo-600 to-purple-600 bg-clip-text text-transparent">
                    {result.total_score} / 10
                  </span>
                </div>

              </div>
            );
          })}
        </div>
      )}

    </div>
  );
}

export default Results;
